#define U (unsigned char)

int	ft_strcmp(const char *s1, const char *s2)
{
	while (*s1 && *s2)
		if (*s1++ != *s2++)
			return (U*(s1 - 1) - U*(s2 - 1));
	if (!*s1 && *s2)
		return (U*s1 - U*s2);
	if (!*s2 && *s1)
		return (U*s1 - U*s1);
	return (0);
}
